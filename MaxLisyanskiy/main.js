/***** While *****/
function handleWhile() {
    let i = 1;

    while (i < 20) {
        console.log(i);
        i++
    }
}
handleWhile();

function handleDoWhile() {
    let i = 1;

    do {
        console.log(i);
        i++;
    } while (i < 20);
}
handleDoWhile()


/***** For *****/
function handleFor() {
    for (let i = 1; i < 20; i++) {
        console.log(i);
    }
}
handleFor();