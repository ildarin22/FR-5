// Вывести в консоль числа от 1 до 19 с помощью цикла

let i = 0;
while (i < 19) {
    i++;
    console.log(i);
}

// Сделать то же самое но с проверкой условия после тела цикла (do ... while)

let k = 0;
do {
    i++
    console.log(k)
} while (k < 19);

// Перепишите свои while циклы на циклы for

for (let j = 1; j < 20; j++) {
    console.log(j)
}